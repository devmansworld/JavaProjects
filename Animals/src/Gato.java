public class Gato extends Animal {

    String nombre;
    String raza;
    String color;
    Integer edad;

public Gato(){}
public Gato(String nombre, String raza){
    this.nombre = nombre;
    this.raza = raza;
}

public Gato(String nombre, String raza, String color, Integer edad)
{
    this.nombre = nombre;
    this.raza = raza;
    this.color = color;
    this.edad = edad;
}




}

